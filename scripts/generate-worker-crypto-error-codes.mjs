import { readFileSync, renameSync, unlinkSync, writeFileSync } from 'node:fs';
import { dirname, relative, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const scriptDir = dirname(fileURLToPath(import.meta.url));
const repoRoot = resolve(scriptDir, '..');
const sourcePath = resolve(repoRoot, 'crates', 'mosaic-client', 'src', 'lib.rs');
const outputPath = resolve(
  repoRoot,
  'apps',
  'web',
  'src',
  'workers',
  'worker-crypto-error-code.generated.ts',
);
const regenerateCommand = 'node scripts/generate-worker-crypto-error-codes.mjs';
const workerOnlyCodes = [
  {
    name: 'StaleHandle',
    value: 1000,
    docs: ['Operation issued against a handle whose generation no longer matches.'],
  },
  {
    name: 'HandleNotFound',
    value: 1001,
    docs: ["Handle ID is not registered with the worker's handle registry."],
  },
  {
    name: 'HandleWrongKind',
    value: 1002,
    docs: ['Handle ID exists but refers to a handle of a different kind than expected.'],
  },
  {
    name: 'ClosedHandle',
    value: 1003,
    docs: ['Handle has been closed; subsequent operations are rejected.'],
  },
  {
    name: 'WorkerNotInitialized',
    value: 1004,
    docs: ['Worker has not been bootstrapped or has been cleared via clear().'],
  },
];

function extractEnumBody(source) {
  const enumMarker = 'pub enum ClientErrorCode';
  const enumIndex = source.indexOf(enumMarker);
  if (enumIndex === -1) {
    throw new Error('Could not find `pub enum ClientErrorCode` in Rust source.');
  }

  const attributes = source.slice(Math.max(0, enumIndex - 512), enumIndex);
  if (!attributes.includes('#[repr(u16)]')) {
    throw new Error('ClientErrorCode must remain #[repr(u16)] for stable TS generation.');
  }

  const bodyStart = source.indexOf('{', enumIndex);
  if (bodyStart === -1) {
    throw new Error('Could not find ClientErrorCode enum body start.');
  }

  let depth = 0;
  for (let i = bodyStart; i < source.length; i += 1) {
    const char = source[i];
    if (char === '{') {
      depth += 1;
    } else if (char === '}') {
      depth -= 1;
      if (depth === 0) {
        return source.slice(bodyStart + 1, i);
      }
    }
  }

  throw new Error('Could not find ClientErrorCode enum body end.');
}

function parseClientErrorCodes(source) {
  const body = extractEnumBody(source);
  const codes = [];
  const seenNames = new Set();
  const seenValues = new Set();

  for (const rawLine of body.split(/\r?\n/u)) {
    const line = rawLine.replace(/\/\/.*$/u, '').trim();
    if (line.length === 0 || line.startsWith('#[')) {
      continue;
    }

    const match = line.match(/^([A-Za-z][A-Za-z0-9_]*)\s*=\s*(\d+)\s*,?$/u);
    if (!match) {
      continue;
    }

    const [, name, valueText] = match;
    const value = Number.parseInt(valueText, 10);
    if (!Number.isInteger(value) || value < 0 || value > 65535) {
      throw new Error(`Invalid u16 discriminant for ClientErrorCode::${name}: ${valueText}`);
    }
    if (seenNames.has(name)) {
      throw new Error(`Duplicate ClientErrorCode variant name: ${name}`);
    }
    if (seenValues.has(value)) {
      throw new Error(`Duplicate ClientErrorCode discriminant: ${value}`);
    }

    seenNames.add(name);
    seenValues.add(value);
    codes.push({ name, value });
  }

  if (codes.length === 0) {
    throw new Error('No ClientErrorCode variants with explicit discriminants were parsed.');
  }
  for (const code of codes) {
    if (code.value >= 1000) {
      throw new Error(
        `ClientErrorCode::${code.name} = ${code.value} collides with TypeScript worker-only range.`,
      );
    }
  }

  return codes;
}

function renderGeneratedFile(codes) {
  const lines = [
    '// AUTO-GENERATED by R-C7-3. Do NOT edit by hand.',
    '// Source: crates/mosaic-client/src/lib.rs::ClientErrorCode',
    `// Regenerate with: ${regenerateCommand}`,
    '//',
    '// Drift is enforced by `crates/mosaic-uniffi/tests/error_code_parity.rs` and',
    '// the CI step in `.github/workflows/tests.yml`.',
    '',
    '/**',
    ' * Stable numeric error codes generated from `mosaic_client::ClientErrorCode`,',
    ' * plus TypeScript worker-only codes for handle-lifecycle failures.',
    ' *',
    ' * Worker-only codes start at 1000 to avoid collisions with Rust codes.',
    ' */',
    'export enum WorkerCryptoErrorCode {',
  ];

  for (const code of codes) {
    lines.push(`  ${code.name} = ${code.value},`);
  }

  lines.push('', '  // Worker-only error codes start at 1000.');
  for (const code of workerOnlyCodes) {
    for (const doc of code.docs) {
      lines.push(`  /** ${doc} */`);
    }
    lines.push(`  ${code.name} = ${code.value},`);
  }

  lines.push('}', '');
  return `${lines.join('\n')}`;
}

const checkOnly = process.argv.includes('--check');
const rustSource = readFileSync(sourcePath, 'utf8');
const rendered = renderGeneratedFile(parseClientErrorCodes(rustSource));

if (checkOnly) {
  const current = readFileSync(outputPath, 'utf8');
  if (current !== rendered) {
    const displayPath = relative(repoRoot, outputPath);
    console.error(
      `${displayPath} is out of date. Run \`${regenerateCommand}\` to update generated error codes.`,
    );
    process.exit(1);
  }
} else {
  const tempOutputPath = `${outputPath}.${process.pid}.tmp`;
  try {
    writeFileSync(tempOutputPath, rendered, 'utf8');
    renameSync(tempOutputPath, outputPath);
  } catch (error) {
    try {
      unlinkSync(tempOutputPath);
    } catch {
      // Ignore cleanup failures so the original write error is reported.
    }
    throw error;
  }
}
