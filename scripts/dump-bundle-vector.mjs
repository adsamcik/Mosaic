// Generate the TS golden vector for the Rust mosaic-crypto sharing tests.
// Inlines the bundle protocol from libs/crypto/src/sharing.ts so it can run
// against a plain Node + libsodium-wrappers-sumo install without going
// through tsc/vitest. Output is a Rust .rs.inc file with deterministic
// constants used by `crates/mosaic-crypto/tests/sharing.rs`.
//
// Usage (from libs/crypto/ where the dependency is installed):
//   node ../../scripts/dump-bundle-vector.mjs > \
//     ../../crates/mosaic-crypto/tests/sharing_vector.rs.inc

import sodium from 'libsodium-wrappers-sumo';

await sodium.ready;

const BUNDLE_SIGN_CONTEXT = 'Mosaic_EpochBundle_v1';
const BUNDLE_VERSION = 1;

const ALBUM_ID = 'ts-golden-album';
const EPOCH_ID = 11;

const recipientSeed = new Uint8Array([
  0x11, 0x22, 0x33, 0x44, 0x55, 0x66, 0x77, 0x88, 0x99, 0xaa, 0xbb, 0xcc, 0xdd, 0xee, 0xff, 0x00,
  0x10, 0x20, 0x30, 0x40, 0x50, 0x60, 0x70, 0x80, 0x90, 0xa0, 0xb0, 0xc0, 0xd0, 0xe0, 0xf0, 0x00,
]);
const ownerSeed = new Uint8Array([
  0xa1, 0xa2, 0xa3, 0xa4, 0xa5, 0xa6, 0xa7, 0xa8, 0xa9, 0xaa, 0xab, 0xac, 0xad, 0xae, 0xaf, 0xb0,
  0xb1, 0xb2, 0xb3, 0xb4, 0xb5, 0xb6, 0xb7, 0xb8, 0xb9, 0xba, 0xbb, 0xbc, 0xbd, 0xbe, 0xbf, 0xc0,
]);
const epochSeed = new Uint8Array([
  0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a, 0x0b, 0x0c, 0x0d, 0x0e, 0x0f, 0x10,
  0x11, 0x12, 0x13, 0x14, 0x15, 0x16, 0x17, 0x18, 0x19, 0x1a, 0x1b, 0x1c, 0x1d, 0x1e, 0x1f, 0x20,
]);
const epochSignSeed = new Uint8Array([
  0x70, 0x71, 0x72, 0x73, 0x74, 0x75, 0x76, 0x77, 0x78, 0x79, 0x7a, 0x7b, 0x7c, 0x7d, 0x7e, 0x7f,
  0x80, 0x81, 0x82, 0x83, 0x84, 0x85, 0x86, 0x87, 0x88, 0x89, 0x8a, 0x8b, 0x8c, 0x8d, 0x8e, 0x8f,
]);

function deriveIdentity(seed) {
  const ed = sodium.crypto_sign_seed_keypair(seed);
  const x25519Pub = sodium.crypto_sign_ed25519_pk_to_curve25519(ed.publicKey);
  const x25519Sec = sodium.crypto_sign_ed25519_sk_to_curve25519(ed.privateKey);
  return {
    ed25519: { publicKey: ed.publicKey, secretKey: ed.privateKey },
    x25519: { publicKey: x25519Pub, secretKey: x25519Sec },
  };
}

function toBase64(data) {
  return sodium.to_base64(data, sodium.base64_variants.URLSAFE_NO_PADDING);
}

function concatBytes(...arrays) {
  const total = arrays.reduce((sum, a) => sum + a.length, 0);
  const out = new Uint8Array(total);
  let offset = 0;
  for (const a of arrays) {
    out.set(a, offset);
    offset += a.length;
  }
  return out;
}

const ownerIdentity = deriveIdentity(ownerSeed);
const recipientIdentity = deriveIdentity(recipientSeed);

const signKp = sodium.crypto_sign_seed_keypair(epochSignSeed);

const bundle = {
  version: BUNDLE_VERSION,
  albumId: ALBUM_ID,
  epochId: EPOCH_ID,
  recipientPubkey: recipientIdentity.ed25519.publicKey,
  epochSeed,
  signKeypair: { publicKey: signKp.publicKey, secretKey: signKp.privateKey },
};

const bundleJson = JSON.stringify({
  version: bundle.version,
  albumId: bundle.albumId,
  epochId: bundle.epochId,
  recipientPubkey: toBase64(bundle.recipientPubkey),
  epochSeed: toBase64(bundle.epochSeed),
  signKeypair: {
    publicKey: toBase64(bundle.signKeypair.publicKey),
    secretKey: toBase64(bundle.signKeypair.secretKey),
  },
});

const sealed = sodium.crypto_box_seal(
  new TextEncoder().encode(bundleJson),
  recipientIdentity.x25519.publicKey,
);

const contextBytes = new TextEncoder().encode(BUNDLE_SIGN_CONTEXT);
const toSign = concatBytes(contextBytes, sealed);
const signature = sodium.crypto_sign_detached(toSign, ownerIdentity.ed25519.secretKey);
const sharerPubkey = ownerIdentity.ed25519.publicKey;

function rustBytes(label, bytes, asSlice = false) {
  const lines = [];
  for (let i = 0; i < bytes.length; i += 12) {
    const chunk = Array.from(bytes.slice(i, i + 12))
      .map((b) => '0x' + b.toString(16).padStart(2, '0'))
      .join(', ');
    lines.push('    ' + chunk + ',');
  }
  if (asSlice) {
    return `const ${label}: &[u8] = &[\n${lines.join('\n')}\n];`;
  }
  return `const ${label}: [u8; ${bytes.length}] = [\n${lines.join('\n')}\n];`;
}

const out = [
  '// Generated by scripts/dump-bundle-vector.mjs against the TypeScript',
  '// bundle protocol (libsodium crypto_box_seal + Ed25519 detached sign).',
  '// Do not edit by hand; regenerate if the TS protocol changes.',
  '',
  rustBytes('TS_VECTOR_EPOCH_SEED', epochSeed),
  '',
  rustBytes('TS_VECTOR_SIGN_PUBLIC_KEY', signKp.publicKey),
  '',
  rustBytes('TS_VECTOR_SHARER_PUBKEY', sharerPubkey),
  '',
  rustBytes('TS_VECTOR_SIGNATURE', signature),
  '',
  rustBytes('TS_VECTOR_SEALED', sealed, true),
  '',
].join('\n');

process.stdout.write(out);
