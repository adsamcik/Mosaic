fn detect_format(bytes: &[u8]) -> Result<MediaFormat, MosaicMediaError> {
    if bytes.starts_with(&[0xff, 0xd8]) {
        return Ok(MediaFormat::Jpeg);
    }
    if bytes.starts_with(PNG_SIGNATURE) {
        return Ok(MediaFormat::Png);
    }
    if bytes.len() >= 12 && bytes.starts_with(b"RIFF") && bytes.get(8..12) == Some(b"WEBP") {
        return Ok(MediaFormat::WebP);
    }
    Err(MosaicMediaError::UnsupportedFormat)
}

fn validate_dimensions(width: u32, height: u32) -> Result<(u32, u32), MosaicMediaError> {
    if width == 0 || height == 0 {
        return Err(MosaicMediaError::InvalidDimensions);
    }

    let pixels = u64::from(width) * u64::from(height);
    if pixels > u64::from(MAX_IMAGE_PIXELS) {
        return Err(MosaicMediaError::InvalidDimensions);
    }

    Ok((width, height))
}

fn fit_inside_square(width: u32, height: u32, max_dimension: u32) -> (u32, u32) {
    if width <= max_dimension && height <= max_dimension {
        return (width, height);
    }

    if width >= height {
        (
            max_dimension,
            scale_secondary_dimension(height, max_dimension, width),
        )
    } else {
        (
            scale_secondary_dimension(width, max_dimension, height),
            max_dimension,
        )
    }
}

fn scale_secondary_dimension(dimension: u32, max_dimension: u32, dominant_dimension: u32) -> u32 {
    let scaled = (u64::from(dimension) * u64::from(max_dimension)) / u64::from(dominant_dimension);
    match u32::try_from(scaled.clamp(1, u64::from(max_dimension))) {
        Ok(value) => value,
        Err(_) => max_dimension,
    }
}

fn extract_jpeg_exif_orientation(bytes: &[u8]) -> Result<u8, MosaicMediaError> {
    if !bytes.starts_with(&[0xff, 0xd8]) {
        return Err(MosaicMediaError::InvalidJpeg);
    }

    let mut offset = 2;
    while offset < bytes.len() {
        if bytes.get(offset) != Some(&0xff) {
            return Err(MosaicMediaError::InvalidJpeg);
        }
        offset += 1;

        while bytes.get(offset) == Some(&0xff) {
            offset += 1;
        }

        let marker = match bytes.get(offset) {
            Some(value) => *value,
            None => return Err(MosaicMediaError::InvalidJpeg),
        };
        offset += 1;

        if marker == 0xda || marker == 0xd9 {
            return Ok(NORMAL_EXIF_ORIENTATION);
        }

        if marker == 0x01 || (0xd0..=0xd7).contains(&marker) {
            continue;
        }

        let length_end = match offset.checked_add(2) {
            Some(value) => value,
            None => return Err(MosaicMediaError::InvalidJpeg),
        };
        let length_bytes = match bytes.get(offset..length_end) {
            Some(value) => value,
            None => return Err(MosaicMediaError::InvalidJpeg),
        };
        let segment_len = usize::from(u16::from_be_bytes([length_bytes[0], length_bytes[1]]));
        if segment_len < 2 {
            return Err(MosaicMediaError::InvalidJpeg);
        }

        let segment_end = match offset.checked_add(segment_len) {
            Some(value) => value,
            None => return Err(MosaicMediaError::InvalidJpeg),
        };
        let payload_start = offset + 2;
        let payload = match bytes.get(payload_start..segment_end) {
            Some(value) => value,
            None => return Err(MosaicMediaError::InvalidJpeg),
        };

        if marker == 0xe1 && payload.starts_with(b"Exif\0\0") {
            return Ok(parse_tiff_orientation(&payload[6..]));
        }

        offset = segment_end;
    }

    Ok(NORMAL_EXIF_ORIENTATION)
}

fn parse_tiff_orientation(tiff: &[u8]) -> u8 {
    let little_endian = match tiff.get(0..2) {
        Some(b"II") => true,
        Some(b"MM") => false,
        _ => return NORMAL_EXIF_ORIENTATION,
    };

    if read_tiff_u16(tiff, 2, little_endian) != Some(42) {
        return NORMAL_EXIF_ORIENTATION;
    }

    let ifd_offset =
        match read_tiff_u32(tiff, 4, little_endian).and_then(|value| usize::try_from(value).ok()) {
            Some(value) => value,
            None => return NORMAL_EXIF_ORIENTATION,
        };
    let entry_count = match read_tiff_u16(tiff, ifd_offset, little_endian) {
        Some(value) => value,
        None => return NORMAL_EXIF_ORIENTATION,
    };
    // Defense-in-depth: an adversarial EXIF payload may declare up to
    // `u16::MAX` IFD entries. Treat anything beyond `MAX_TIFF_IFD_ENTRIES`
    // as malformed and fall back to normal orientation, matching the
    // existing fallback policy for other unparseable TIFF metadata.
    if entry_count > MAX_TIFF_IFD_ENTRIES {
        return NORMAL_EXIF_ORIENTATION;
    }
    let entries_start = match ifd_offset.checked_add(2) {
        Some(value) => value,
        None => return NORMAL_EXIF_ORIENTATION,
    };

    for entry_index in 0..entry_count {
        let entry_offset = match entries_start.checked_add(usize::from(entry_index) * 12) {
            Some(value) => value,
            None => return NORMAL_EXIF_ORIENTATION,
        };
        let entry_end = match entry_offset.checked_add(12) {
            Some(value) => value,
            None => return NORMAL_EXIF_ORIENTATION,
        };
        if tiff.get(entry_offset..entry_end).is_none() {
            return NORMAL_EXIF_ORIENTATION;
        }

        let tag = read_tiff_u16(tiff, entry_offset, little_endian);
        if tag != Some(0x0112) {
            continue;
        }

        let value_type = read_tiff_u16(tiff, entry_offset + 2, little_endian);
        let value_count = read_tiff_u32(tiff, entry_offset + 4, little_endian);
        if value_type != Some(3) || value_count != Some(1) {
            return NORMAL_EXIF_ORIENTATION;
        }

        let orientation = read_tiff_u16(tiff, entry_offset + 8, little_endian)
            .unwrap_or(u16::from(NORMAL_EXIF_ORIENTATION));
        return canonical_exif_orientation(orientation);
    }

    NORMAL_EXIF_ORIENTATION
}

fn read_tiff_u16(bytes: &[u8], offset: usize, little_endian: bool) -> Option<u16> {
    let end = offset.checked_add(2)?;
    let data = bytes.get(offset..end)?;
    let raw = [data[0], data[1]];
    Some(if little_endian {
        u16::from_le_bytes(raw)
    } else {
        u16::from_be_bytes(raw)
    })
}

fn read_tiff_u32(bytes: &[u8], offset: usize, little_endian: bool) -> Option<u32> {
    let end = offset.checked_add(4)?;
    let data = bytes.get(offset..end)?;
    let raw = [data[0], data[1], data[2], data[3]];
    Some(if little_endian {
        u32::from_le_bytes(raw)
    } else {
        u32::from_be_bytes(raw)
    })
}

fn canonical_exif_orientation(orientation: u16) -> u8 {
    if (1..=8).contains(&orientation) {
        orientation as u8
    } else {
        NORMAL_EXIF_ORIENTATION
    }
}

fn orientation_swaps_dimensions(orientation: u8) -> bool {
    matches!(orientation, 5..=8)
}

fn inspect_jpeg_dimensions(bytes: &[u8]) -> Result<(u32, u32), MosaicMediaError> {
    if !bytes.starts_with(&[0xff, 0xd8]) {
        return Err(MosaicMediaError::InvalidJpeg);
    }

    let mut offset = 2;
    while offset < bytes.len() {
        if bytes.get(offset) != Some(&0xff) {
            return Err(MosaicMediaError::InvalidJpeg);
        }
        offset += 1;

        while bytes.get(offset) == Some(&0xff) {
            offset += 1;
        }

        let marker = match bytes.get(offset) {
            Some(value) => *value,
            None => return Err(MosaicMediaError::InvalidJpeg),
        };
        offset += 1;

        if marker == 0xda || marker == 0xd9 {
            return Err(MosaicMediaError::InvalidJpeg);
        }

        if marker == 0x01 || (0xd0..=0xd7).contains(&marker) {
            continue;
        }

        let length_end = match offset.checked_add(2) {
            Some(value) => value,
            None => return Err(MosaicMediaError::InvalidJpeg),
        };
        let length_bytes = match bytes.get(offset..length_end) {
            Some(value) => value,
            None => return Err(MosaicMediaError::InvalidJpeg),
        };
        let segment_len = usize::from(u16::from_be_bytes([length_bytes[0], length_bytes[1]]));
        if segment_len < 2 {
            return Err(MosaicMediaError::InvalidJpeg);
        }

        let segment_end = match offset.checked_add(segment_len) {
            Some(value) => value,
            None => return Err(MosaicMediaError::InvalidJpeg),
        };
        let payload_start = offset + 2;
        let payload = match bytes.get(payload_start..segment_end) {
            Some(value) => value,
            None => return Err(MosaicMediaError::InvalidJpeg),
        };

        if is_jpeg_start_of_frame(marker) {
            if payload.len() < 5 {
                return Err(MosaicMediaError::InvalidJpeg);
            }
            let height = u32::from(u16::from_be_bytes([payload[1], payload[2]]));
            let width = u32::from(u16::from_be_bytes([payload[3], payload[4]]));
            // DNL-deferred JPEG height uses zero in SOF and requires scanning past
            // the first entropy-coded scan; this header-only gate rejects it.
            return validate_dimensions(width, height);
        }

        offset = segment_end;
    }

    Err(MosaicMediaError::InvalidJpeg)
}

fn is_jpeg_start_of_frame(marker: u8) -> bool {
    matches!(
        marker,
        0xc0 | 0xc1 | 0xc2 | 0xc3 | 0xc5 | 0xc6 | 0xc7 | 0xc9 | 0xca | 0xcb | 0xcd | 0xce | 0xcf
    )
}

fn inspect_png_dimensions(bytes: &[u8]) -> Result<(u32, u32), MosaicMediaError> {
    if !bytes.starts_with(PNG_SIGNATURE) {
        return Err(MosaicMediaError::InvalidPng);
    }

    let mut offset = PNG_SIGNATURE.len();
    let length_bytes = match bytes.get(offset..offset + 4) {
        Some(value) => value,
        None => return Err(MosaicMediaError::InvalidPng),
    };
    let payload_len = u32::from_be_bytes([
        length_bytes[0],
        length_bytes[1],
        length_bytes[2],
        length_bytes[3],
    ]);
    if payload_len != 13 {
        return Err(MosaicMediaError::InvalidPng);
    }
    offset += 4;

    if bytes.get(offset..offset + 4) != Some(b"IHDR") {
        return Err(MosaicMediaError::InvalidPng);
    }
    offset += 4;

    let payload = match bytes.get(offset..offset + 13) {
        Some(value) => value,
        None => return Err(MosaicMediaError::InvalidPng),
    };
    let width = u32::from_be_bytes([payload[0], payload[1], payload[2], payload[3]]);
    let height = u32::from_be_bytes([payload[4], payload[5], payload[6], payload[7]]);
    validate_dimensions(width, height)
}

fn inspect_webp_dimensions(bytes: &[u8]) -> Result<(u32, u32), MosaicMediaError> {
    if bytes.len() < 12 || !bytes.starts_with(b"RIFF") || bytes.get(8..12) != Some(b"WEBP") {
        return Err(MosaicMediaError::InvalidWebP);
    }

    let mut offset = 12;
    while offset < bytes.len() {
        let kind_end = match offset.checked_add(4) {
            Some(value) => value,
            None => return Err(MosaicMediaError::InvalidWebP),
        };
        let chunk_type = match bytes.get(offset..kind_end) {
            Some(value) => value,
            None => return Err(MosaicMediaError::InvalidWebP),
        };
        offset = kind_end;

        let size_end = match offset.checked_add(4) {
            Some(value) => value,
            None => return Err(MosaicMediaError::InvalidWebP),
        };
        let size_bytes = match bytes.get(offset..size_end) {
            Some(value) => value,
            None => return Err(MosaicMediaError::InvalidWebP),
        };
        let payload_len_u32 =
            u32::from_le_bytes([size_bytes[0], size_bytes[1], size_bytes[2], size_bytes[3]]);
        let payload_len = match usize::try_from(payload_len_u32) {
            Ok(value) => value,
            Err(_) => return Err(MosaicMediaError::InvalidWebP),
        };
        offset = size_end;

        let payload_end = match offset.checked_add(payload_len) {
            Some(value) => value,
            None => return Err(MosaicMediaError::InvalidWebP),
        };
        let padded_end = match payload_end.checked_add(payload_len % 2) {
            Some(value) => value,
            None => return Err(MosaicMediaError::InvalidWebP),
        };
        let payload = match bytes.get(offset..payload_end) {
            Some(value) => value,
            None => return Err(MosaicMediaError::InvalidWebP),
        };
        if bytes.get(payload_end..padded_end).is_none() {
            return Err(MosaicMediaError::InvalidWebP);
        }

        if let Some(dimensions) = inspect_webp_dimension_chunk(chunk_type, payload)? {
            return Ok(dimensions);
        }

        offset = padded_end;
    }

    Err(MosaicMediaError::InvalidWebP)
}

fn inspect_webp_dimension_chunk(
    chunk_type: &[u8],
    payload: &[u8],
) -> Result<Option<(u32, u32)>, MosaicMediaError> {
    match chunk_type {
        b"VP8 " => inspect_vp8_dimensions(payload).map(Some),
        b"VP8L" => inspect_vp8l_dimensions(payload).map(Some),
        b"VP8X" => inspect_vp8x_dimensions(payload).map(Some),
        _ => Ok(None),
    }
}

fn inspect_vp8_dimensions(payload: &[u8]) -> Result<(u32, u32), MosaicMediaError> {
    if payload.len() < 10 || payload.get(3..6) != Some(&[0x9d, 0x01, 0x2a]) {
        return Err(MosaicMediaError::InvalidWebP);
    }

    let width = u32::from(u16::from_le_bytes([payload[6], payload[7]]) & 0x3fff);
    let height = u32::from(u16::from_le_bytes([payload[8], payload[9]]) & 0x3fff);
    validate_dimensions(width, height)
}

fn inspect_vp8l_dimensions(payload: &[u8]) -> Result<(u32, u32), MosaicMediaError> {
    if payload.len() < 5 || payload[0] != 0x2f {
        return Err(MosaicMediaError::InvalidWebP);
    }

    let bits = u32::from_le_bytes([payload[1], payload[2], payload[3], payload[4]]);
    let width = (bits & 0x3fff) + 1;
    let height = ((bits >> 14) & 0x3fff) + 1;
    validate_dimensions(width, height)
}

fn inspect_vp8x_dimensions(payload: &[u8]) -> Result<(u32, u32), MosaicMediaError> {
    if payload.len() < 10 {
        return Err(MosaicMediaError::InvalidWebP);
    }

    let width = read_u24_le(&[payload[4], payload[5], payload[6]]) + 1;
    let height = read_u24_le(&[payload[7], payload[8], payload[9]]) + 1;
    validate_dimensions(width, height)
}

fn read_u24_le(bytes: &[u8; 3]) -> u32 {
    u32::from(bytes[0]) | (u32::from(bytes[1]) << 8) | (u32::from(bytes[2]) << 16)
}

fn strip_jpeg_metadata(bytes: &[u8]) -> Result<StrippedMedia, MosaicMediaError> {
    if !bytes.starts_with(&[0xff, 0xd8]) {
        return Err(MosaicMediaError::InvalidJpeg);
    }

    // Pre-allocated to the input size so `extend_from_slice` never reallocates
    // and orphans a partially-written plaintext buffer outside the wrapper.
    // On any error path the wrapper's `Drop` zeroes the accumulated bytes.
    let mut output: Zeroizing<Vec<u8>> = Zeroizing::new(Vec::with_capacity(bytes.len()));
    let mut removed = Vec::new();
    output.extend_from_slice(&bytes[..2]);

    let mut offset = 2;
    while offset < bytes.len() {
        let marker_start = offset;
        if bytes.get(offset) != Some(&0xff) {
            return Err(MosaicMediaError::InvalidJpeg);
        }
        offset += 1;

        while bytes.get(offset) == Some(&0xff) {
            offset += 1;
        }

        let marker = match bytes.get(offset) {
            Some(value) => *value,
            None => return Err(MosaicMediaError::InvalidJpeg),
        };
        offset += 1;

        if marker == 0xda {
            output.extend_from_slice(&bytes[marker_start..]);
            return Ok(StrippedMedia {
                bytes: std::mem::take(&mut *output),
                removed,
            });
        }

        if marker == 0xd9 || marker == 0x01 || (0xd0..=0xd7).contains(&marker) {
            output.extend_from_slice(&bytes[marker_start..offset]);
            continue;
        }

        let length_end = match offset.checked_add(2) {
            Some(value) => value,
            None => return Err(MosaicMediaError::InvalidJpeg),
        };
        let length_bytes = match bytes.get(offset..length_end) {
            Some(value) => value,
            None => return Err(MosaicMediaError::InvalidJpeg),
        };
        let segment_len = usize::from(u16::from_be_bytes([length_bytes[0], length_bytes[1]]));
        if segment_len < 2 {
            return Err(MosaicMediaError::InvalidJpeg);
        }

        let segment_end = match offset.checked_add(segment_len) {
            Some(value) => value,
            None => return Err(MosaicMediaError::InvalidJpeg),
        };
        let payload_start = offset + 2;
        let payload = match bytes.get(payload_start..segment_end) {
            Some(value) => value,
            None => return Err(MosaicMediaError::InvalidJpeg),
        };

        if let Some(kind) = classify_jpeg_metadata(marker, payload) {
            removed.push(kind);
        } else {
            output.extend_from_slice(&bytes[marker_start..segment_end]);
        }
        offset = segment_end;
    }

    Ok(StrippedMedia {
        bytes: std::mem::take(&mut *output),
        removed,
    })
}

fn classify_jpeg_metadata(marker: u8, payload: &[u8]) -> Option<MetadataKind> {
    match marker {
        0xe1 if payload.starts_with(b"Exif\0\0") => Some(MetadataKind::Exif),
        0xe1 if payload.starts_with(b"http://ns.adobe.com/xap/1.0/") => Some(MetadataKind::Xmp),
        0xe1 if payload.starts_with(b"http://ns.adobe.com/xmp/extension/\0") => {
            Some(MetadataKind::Xmp)
        }
        0xe2 if payload.starts_with(b"ICC_PROFILE\0") => Some(MetadataKind::ColorProfile),
        0xed => Some(MetadataKind::Iptc),
        0xfe => Some(MetadataKind::Comment),
        // APPn segments are application-defined and routinely carry device,
        // editor, thumbnail, caption, or color metadata outside EXIF/XMP/IPTC.
        0xe0..=0xef => Some(MetadataKind::RenderingHint),
        _ => None,
    }
}

fn strip_png_metadata(bytes: &[u8]) -> Result<StrippedMedia, MosaicMediaError> {
    if !bytes.starts_with(PNG_SIGNATURE) {
        return Err(MosaicMediaError::InvalidPng);
    }

    // Pre-allocated to the input size so `extend_from_slice` never reallocates
    // and orphans a partially-written plaintext buffer outside the wrapper.
    // On any error path the wrapper's `Drop` zeroes the accumulated bytes.
    let mut output: Zeroizing<Vec<u8>> = Zeroizing::new(Vec::with_capacity(bytes.len()));
    let mut removed = Vec::new();
    output.extend_from_slice(PNG_SIGNATURE);

    let mut offset = PNG_SIGNATURE.len();
    while offset < bytes.len() {
        let chunk_start = offset;
        let length_end = match offset.checked_add(4) {
            Some(value) => value,
            None => return Err(MosaicMediaError::InvalidPng),
        };
        let length_bytes = match bytes.get(offset..length_end) {
            Some(value) => value,
            None => return Err(MosaicMediaError::InvalidPng),
        };
        let payload_len_u32 = u32::from_be_bytes([
            length_bytes[0],
            length_bytes[1],
            length_bytes[2],
            length_bytes[3],
        ]);
        let payload_len = match usize::try_from(payload_len_u32) {
            Ok(value) => value,
            Err(_) => return Err(MosaicMediaError::InvalidPng),
        };
        offset = length_end;

        let chunk_type_end = match offset.checked_add(4) {
            Some(value) => value,
            None => return Err(MosaicMediaError::InvalidPng),
        };
        let chunk_type = match bytes.get(offset..chunk_type_end) {
            Some(value) => value,
            None => return Err(MosaicMediaError::InvalidPng),
        };
        offset = chunk_type_end;

        let payload_end = match offset.checked_add(payload_len) {
            Some(value) => value,
            None => return Err(MosaicMediaError::InvalidPng),
        };
        let crc_end = match payload_end.checked_add(4) {
            Some(value) => value,
            None => return Err(MosaicMediaError::InvalidPng),
        };
        if bytes.get(offset..payload_end).is_none() || bytes.get(payload_end..crc_end).is_none() {
            return Err(MosaicMediaError::InvalidPng);
        }

        if let Some(kind) = classify_png_metadata(chunk_type) {
            removed.push(kind);
        } else {
            output.extend_from_slice(&bytes[chunk_start..crc_end]);
        }
        offset = crc_end;
    }

    Ok(StrippedMedia {
        bytes: std::mem::take(&mut *output),
        removed,
    })
}

fn classify_png_metadata(chunk_type: &[u8]) -> Option<MetadataKind> {
    match chunk_type {
        b"eXIf" => Some(MetadataKind::Exif),
        b"iTXt" | b"tEXt" | b"zTXt" => Some(MetadataKind::Text),
        b"tIME" => Some(MetadataKind::Timestamp),
        b"iCCP" | b"sRGB" | b"cHRM" | b"gAMA" => Some(MetadataKind::ColorProfile),
        b"pHYs" => Some(MetadataKind::PhysicalDimensions),
        b"sPLT" => Some(MetadataKind::SuggestedPalette),
        b"bKGD" | b"hIST" => Some(MetadataKind::RenderingHint),
        _ => None,
    }
}

fn strip_webp_metadata(bytes: &[u8]) -> Result<StrippedMedia, MosaicMediaError> {
    if bytes.len() < 12 || !bytes.starts_with(b"RIFF") || bytes.get(8..12) != Some(b"WEBP") {
        return Err(MosaicMediaError::InvalidWebP);
    }

    // Pre-allocated to the input size so `extend_from_slice` never reallocates
    // and orphans a partially-written plaintext buffer outside the wrapper.
    // On any error path the wrapper's `Drop` zeroes the accumulated bytes.
    let mut output: Zeroizing<Vec<u8>> = Zeroizing::new(Vec::with_capacity(bytes.len()));
    output.extend_from_slice(b"RIFF\0\0\0\0WEBP");
    let mut removed = Vec::new();
    let mut offset = 12;

    while offset < bytes.len() {
        let chunk_start = offset;
        let kind_end = match offset.checked_add(4) {
            Some(value) => value,
            None => return Err(MosaicMediaError::InvalidWebP),
        };
        let chunk_type = match bytes.get(offset..kind_end) {
            Some(value) => value,
            None => return Err(MosaicMediaError::InvalidWebP),
        };
        offset = kind_end;

        let size_end = match offset.checked_add(4) {
            Some(value) => value,
            None => return Err(MosaicMediaError::InvalidWebP),
        };
        let size_bytes = match bytes.get(offset..size_end) {
            Some(value) => value,
            None => return Err(MosaicMediaError::InvalidWebP),
        };
        let payload_len_u32 =
            u32::from_le_bytes([size_bytes[0], size_bytes[1], size_bytes[2], size_bytes[3]]);
        let payload_len = match usize::try_from(payload_len_u32) {
            Ok(value) => value,
            Err(_) => return Err(MosaicMediaError::InvalidWebP),
        };
        offset = size_end;

        let payload_end = match offset.checked_add(payload_len) {
            Some(value) => value,
            None => return Err(MosaicMediaError::InvalidWebP),
        };
        let padded_end = match payload_end.checked_add(payload_len % 2) {
            Some(value) => value,
            None => return Err(MosaicMediaError::InvalidWebP),
        };
        if bytes.get(offset..payload_end).is_none() || bytes.get(payload_end..padded_end).is_none()
        {
            return Err(MosaicMediaError::InvalidWebP);
        }

        if let Some(kind) = classify_webp_metadata(chunk_type) {
            removed.push(kind);
        } else {
            let output_chunk_start = output.len();
            output.extend_from_slice(&bytes[chunk_start..padded_end]);
            if chunk_type == b"VP8X" {
                if let Some(flags) = output.get_mut(output_chunk_start + 8) {
                    *flags &= !WEBP_VP8X_METADATA_FLAGS;
                }
            }
        }
        offset = padded_end;
    }

    let riff_size = match u32::try_from(output.len() - 8) {
        Ok(value) => value,
        Err(_) => return Err(MosaicMediaError::OutputTooLarge),
    };
    output[4..8].copy_from_slice(&riff_size.to_le_bytes());

    Ok(StrippedMedia {
        bytes: std::mem::take(&mut *output),
        removed,
    })
}

fn classify_webp_metadata(chunk_type: &[u8]) -> Option<MetadataKind> {
    match chunk_type {
        b"EXIF" => Some(MetadataKind::Exif),
        b"XMP " => Some(MetadataKind::Xmp),
        b"ICCP" => Some(MetadataKind::ColorProfile),
        _ => None,
    }
}

