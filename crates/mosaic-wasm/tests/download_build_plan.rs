#[test]
fn tier_one_input_returns_invalid_plan_with_detail() -> Result<(), String> {
    let result = download_build_plan_v1(&common::plan_input_cbor(1)?);

    assert_eq!(
        result.code,
        u32::from(ClientErrorCode::DownloadInvalidPlan.as_u16())
    );
    assert!(result.plan_cbor.is_empty());
    assert!(
        result
            .error_detail
            .contains("DisallowedTier:photoId=photo-1,tier=1")
    );
    Ok(())
}
