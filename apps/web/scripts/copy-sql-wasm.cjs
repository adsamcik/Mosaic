/**
 * Copy sql-wasm files from fts5-sql-bundle to public directory and pin a
 * SHA-384 integrity digest of sql-wasm.js for the DB worker to verify at
 * runtime (defense-in-depth against supply-chain or build-time tampering).
 *
 * This script handles cases where npm hasn't fully extracted packages yet
 * (CI environments) and is idempotent: when the source bundle is missing
 * it exits 0 without rewriting the integrity file (so a fresh checkout
 * before `npm install` does not produce a stale digest).
 */
const fs = require('fs');
const path = require('path');
const crypto = require('crypto');

const srcDir = path.join(__dirname, '..', 'node_modules', 'fts5-sql-bundle', 'dist');
const destDir = path.join(__dirname, '..', 'public');
const integrityFile = path.join(
  __dirname,
  '..',
  'src',
  'generated',
  'sql-wasm-integrity.ts',
);
const files = ['sql-wasm.js', 'sql-wasm.wasm'];

// Ensure public directory exists
if (!fs.existsSync(destDir)) {
  fs.mkdirSync(destDir, { recursive: true });
}

// Check if source directory exists. Exit 0 without rewriting the pinned
// digest so a fresh CI checkout (before `npm install`) cannot replace a
// committed integrity file with a stale one.
if (!fs.existsSync(srcDir)) {
  console.warn('[copy-sql-wasm] Warning: fts5-sql-bundle/dist not found. Files will be copied during build.');
  process.exit(0);
}

let copied = 0;
for (const file of files) {
  const src = path.join(srcDir, file);
  const dest = path.join(destDir, file);

  if (fs.existsSync(src)) {
    fs.copyFileSync(src, dest);
    console.log(`[copy-sql-wasm] Copied ${file}`);
    copied++;
  } else {
    console.warn(`[copy-sql-wasm] Warning: ${file} not found in fts5-sql-bundle/dist`);
  }
}

if (copied === 0) {
  console.warn('[copy-sql-wasm] No files copied. Ensure fts5-sql-bundle is properly installed.');
  // Bail out without rewriting the integrity file.
  process.exit(0);
}

// Compute SHA-384 of the freshly-copied sql-wasm.js and emit a typesafe
// constant the DB worker imports at build time. Keeping the digest in a
// generated TypeScript module (rather than a fetched .sha384 sidecar)
// means runtime tampering with the integrity file is impossible without
// rebuilding the worker bundle.
const sqlWasmJsPath = path.join(destDir, 'sql-wasm.js');
if (!fs.existsSync(sqlWasmJsPath)) {
  console.warn('[copy-sql-wasm] sql-wasm.js missing after copy; skipping integrity emission.');
  process.exit(0);
}

const data = fs.readFileSync(sqlWasmJsPath);
const digestBase64 = crypto.createHash('sha384').update(data).digest('base64');
const integrity = `sha384-${digestBase64}`;

const generatedDir = path.dirname(integrityFile);
if (!fs.existsSync(generatedDir)) {
  fs.mkdirSync(generatedDir, { recursive: true });
}

const content = `// AUTO-GENERATED by apps/web/scripts/copy-sql-wasm.cjs — DO NOT EDIT.
// SHA-384 integrity digest of public/sql-wasm.js, pinned at build/postinstall
// time so the DB worker can verify the script bytes before evaluating them
// via new Function(). Mismatch indicates supply-chain compromise of
// fts5-sql-bundle or build-time tampering.
export const SQL_WASM_SHA384 = '${integrity}';
`;

let existing = null;
try {
  existing = fs.readFileSync(integrityFile, 'utf8');
} catch {
  // First run; no existing file.
}

if (existing !== content) {
  fs.writeFileSync(integrityFile, content);
  console.log(`[copy-sql-wasm] Wrote integrity digest -> src/generated/sql-wasm-integrity.ts (${integrity.slice(0, 24)}...)`);
} else {
  console.log('[copy-sql-wasm] Integrity digest unchanged.');
}
